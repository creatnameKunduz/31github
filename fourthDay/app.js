
var month = prompt('Enter month');

switch (month.toLowerCase()) {
    case 'may':
        console.log('spring');
        break;

    case 'june':
        console.log('summer');
        break;

    case 'february':
        console.log('winter');
        break;

    case 'september':
        console.log('autumn');
        break;

    default:
        console.log('Enter correctly something');
        break; // Добавленный оператор break для default
}


