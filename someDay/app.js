const tags = ['li', 'div', 'li', 'p', 'h1', 'p', 'h1', 'div', 'div', 'li', 'h1', 'p', 'h1', 'ol', 'br'];

// Создаем объект для хранения результата
const tagCounts = {};

// Перебираем массив тегов и увеличиваем счетчик для каждого тега
tags.forEach(tag => {
    if (tagCounts[tag]) {
        // Если тег уже есть в объекте, увеличиваем его счетчик
        tagCounts[tag]++;
    } else {
        // Если тег встречается впервые, добавляем его в объект с начальным счетчиком 1
        tagCounts[tag] = 1;
    }
});

// Выводим результат в виде объекта
console.log(tagCounts);
